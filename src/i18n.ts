export function getTranslations(): Translations {
    const config = vscode.workspace.getConfiguration('antigravity-cockpit');
    const langConfig = config.get<string>('language') || 'auto';

    if (langConfig === 'zh-cn') {
        return zh;
    } else if (langConfig === 'zh-tw') {
        return zhTw;
    } else if (langConfig === 'en') {
        return en;
    } else if (langConfig === 'ja') {
        return ja;
    } else if (langConfig === 'ko') {
        return ko;
    } else if (langConfig === 'ru') {
        return ru;
    } else if (langConfig === 'fr') {
        return fr;
    } else if (langConfig === 'es') {
        return es;
    } else if (langConfig === 'de') {
        return de;
    } else if (langConfig === 'it') {
        return it;
    }

    const lang = vscode.env.language.toLowerCase();
    if (lang.startsWith('zh-cn') || lang === 'zh') return zh;
    if (lang.startsWith('zh-tw') || lang.startsWith('zh-hk')) return zhTw;
    if (lang.startsWith('ja')) return ja;
    if (lang.startsWith('ko')) return ko;
    if (lang.startsWith('ru')) return ru;
    if (lang.startsWith('fr')) return fr;
    if (lang.startsWith('es')) return es;
    if (lang.startsWith('de')) return de;
    if (lang.startsWith('it')) return it;
    return en;
}

export function t(key: keyof Translations, ...args: any[]): string {
    const translations = getTranslations();
    let text = translations[key] || key;
    args.forEach((arg, i) => {
        text = text.replace(`{${i}}`, String(arg));
    });
    return text;
}
